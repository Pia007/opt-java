public class IfElse {
    public static void main(String[] args) {
        int group1 = 21;
        int group2 = 75;

        if (group1 == group2) {
            System.out.println("They are the same size");
        } else if (group1 > group2){
            System.out.println("Group 1 is larger");
        } else {
            System.out.println("Group 2 is larger");
        }
    }
}
