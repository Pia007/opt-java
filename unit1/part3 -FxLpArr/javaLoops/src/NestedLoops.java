package src;// useful when working with 2d arrays

public class NestedLoops {
    public static void main(String[] args) {
        for ( int i = 0; i <3; i++) {
            System.out.println("run is: " + i);
            for ( int k = 0; k < 3; k++) {
                System.out.println(k);
            }
        }
    }
}
